@pytest.mark.asyncio
async def test_retry_behavior():
    with patch("backend.agents.synthesis_agent.get_llm_service") as mock_get:
        mock_llm = AsyncMock()
        mock_llm.default_model = "gpt-4o"
        mock_llm.structured_completion = AsyncMock(
            side_effect=[
                SynthesisProviderError("provider error"),
                SynthesisTimeoutError("timeout"),
                _mock_llm_response_limited(),
            ]
        )
        mock_get.return_value = mock_llm

        agent = SynthesisAgent()
        report = await agent.generate_report(
            query="Analyze Apple Inc.",
            tickers=["AAPL"],
            market_data=_base_market_data(),
            news_articles=_base_news(),
        )

        assert isinstance(report, ResearchReport)
        assert mock_llm.structured_completion.call_count == 3


